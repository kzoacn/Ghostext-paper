#!/usr/bin/env python3
from __future__ import annotations

import json
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
RESULTS = ROOT / "results"
OUT = ROOT / "paper" / "sections" / "_eval_autogen.tex"


def _fmt(x: float, n: int = 3) -> str:
    return f"{x:.{n}f}"


def _pct(x: float) -> str:
    return f"{100.0 * x:.1f}"


def _escape_case_id(case_id: str) -> str:
    return case_id.replace("_", r"\_")


def _load_json(path: Path) -> dict:
    return json.loads(path.read_text(encoding="utf-8"))


def _toy_rows(cases: list[dict]) -> str:
    if not cases:
        return r"\multicolumn{5}{l}{No toy backend rows available.} \\"
    rows: list[str] = []
    for row in cases:
        rows.append(
            f"{_escape_case_id(row['id'])} & "
            f"{'Y' if row['success'] else 'N'} & "
            f"{row['packet_len_bytes']} & "
            f"{row['total_tokens']} & "
            f"{_fmt(float(row['bits_per_token']))} \\\\"
        )
    return "\n".join(rows)


def _real_rows(cases: list[dict]) -> str:
    if not cases:
        return r"\multicolumn{5}{l}{Real backend unavailable in this run.} \\"
    rows: list[str] = []
    for row in cases:
        rows.append(
            f"{_escape_case_id(row['id'])} & "
            f"{'Y' if row['success'] else 'N'} & "
            f"{row['packet_len_bytes']} & "
            f"{row['total_tokens']} & "
            f"{_fmt(float(row['bits_per_token']))} \\\\"
        )
    return "\n".join(rows)


def _cmd(name: str, value: str) -> str:
    return f"\\newcommand{{\\{name}}}{{{value}}}"


def main() -> None:
    minimal = _load_json(RESULTS / "minimal_eval_results.json")
    corr = minimal["correctness_and_overhead"]
    fail = minimal["fail_closed"]

    status_file = RESULTS / "real_backend_eval_status.txt"
    real_status = status_file.read_text(encoding="utf-8").strip() if status_file.exists() else "unknown"
    real_path = RESULTS / "real_backend_eval_results.json"
    real = _load_json(real_path) if (real_status == "ok" and real_path.exists()) else None

    if real is not None:
        sanity_error = real["fail_closed_sanity"]["error"] or "UnknownError"
        real_para = (
            "On the real backend, matched-configuration round-trip succeeds on "
            f"{real['n_success']}/{real['n_cases']} ({_pct(real['success_rate'])}\\%). "
            "In this run, mean embedding efficiency is "
            f"{_fmt(float(real['summary']['bits_per_token']['mean']))} bits/token, and mean encode throughput is "
            f"{_fmt(float(real['summary']['encode_tokens_per_second']['mean']), 2)} tokens/s "
            "on the current CPU-only setup. A wrong-prompt sanity check also fails closed with an explicit "
            f"synchronization error (\\texttt{{{sanity_error}}} in this run)."
        )
        real_rows = _real_rows(real["cases"])
    elif real_status == "failed":
        real_para = (
            "On the real backend, this run is marked unavailable because encoding exceeded the configured token "
            "budget in at least one case. We therefore omit numeric claims for this run."
        )
        real_rows = _real_rows([])
    elif real_status == "skipped_no_model":
        real_para = (
            "On the real backend, this run is marked unavailable because no local GGUF model was found in the "
            "evaluation environment."
        )
        real_rows = _real_rows([])
    else:
        real_para = (
            "On the real backend, this run is marked unavailable because status metadata is missing or unknown; "
            "we therefore omit numeric claims for this run."
        )
        real_rows = _real_rows([])

    lines = [
        "% Auto-generated by scripts/generate_eval_tex.py. Do not edit manually.",
        _cmd("ToySuccessCases", str(corr["n_success"])),
        _cmd("ToyTotalCases", str(corr["n_cases"])),
        _cmd("ToySuccessRatePct", _pct(float(corr["success_rate"]))),
        _cmd("ToyFailClosedSuccess", str(fail["n_failed_as_expected"])),
        _cmd("ToyFailClosedTotal", str(fail["n_expected_fail"])),
        _cmd("ToyFailClosedRatePct", _pct(float(fail["fail_closed_rate"]))),
        _cmd("ToyMeanBPT", _fmt(float(corr["overhead_summary"]["bits_per_token"]["mean"]))),
        _cmd("ToyMeanEncodeTPS", _fmt(float(corr["overhead_summary"]["encode_tokens_per_second"]["mean"]), 2)),
        _cmd("ToyTableRows", f"%\n{_toy_rows(corr['cases'])}\n"),
        _cmd("RealEvalStatus", real_status),
        _cmd("RealEvalParagraph", real_para),
        _cmd("RealTableRows", f"%\n{real_rows}\n"),
    ]

    OUT.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(f"wrote {OUT}")


if __name__ == "__main__":
    main()
